package Paquete1;

public class Sumar 
{
	private int a;
	private int b;
	
	public Sumar(){}

	public int hacersumar(int a, int b)
	{
		return a + b;
		
	}
	
	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}
	
	
}
